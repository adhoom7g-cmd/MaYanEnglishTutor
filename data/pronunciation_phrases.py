def get_all_categories():
    """Return list of all pronunciation categories"""
    categories = set(data['category'] for data in PRONUNCIATION_PHRASES.values())
    return list(categories)

def get_phrases_by_category(category):
    """Return phrases for a specific category"""
    result = {}
    for title, data in PRONUNCIATION_PHRASES.items():
        if data['category'] == category:
            result[title] = data
    return result

def get_phrases_by_difficulty(difficulty):
    """Return phrases filtered by difficulty"""
    return {title: data for title, data in PRONUNCIATION_PHRASES.items() 
            if data['difficulty'] == difficulty}

def get_all_phrase_groups():
    """Return list of all phrase group titles"""
    return list(PRONUNCIATION_PHRASES.keys())

def get_phrase_group(title):
    """Return specific phrase group"""
    return PRONUNCIATION_PHRASES.get(title)
